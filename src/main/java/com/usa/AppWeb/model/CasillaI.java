package com.usa.AppWeb.model;

public class CasillaI {
    private String posInicial, posFinal, ficha, color;

    public String getPosInicial() {
        return posInicial;
    }

    public void setPosInicial(String posInicial) {
        this.posInicial = posInicial;
    }

    public String getPosFinal() {
        return posFinal;
    }

    public void setPosFinal(String posFinal) {
        this.posFinal = posFinal;
    }

    public String getFicha() {
        return ficha;
    }

    public void setFicha(String ficha) {
        this.ficha = ficha;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
